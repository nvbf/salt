export async function apiGetTournament(id) {
    const tournaments = await apiGetTournaments()
    log(`tournaments.length ${tournaments.length}`)
    const tournamentArray = tournaments.filter(({ TurneringsId }) => TurneringsId == id)
    if(tournamentArray.length === 0) {
        return {noSuchTournament: "No such tournamentID"}
    }
    return tournamentArray[0]
}
export async function apiGetTournaments() {
    const result = await axios.get(`${API_URL}/tournaments`)
    return getData(result)
}

export async function apiGetNorwegianTournaments() {
    const tournaments = await apiGetTournaments()
    return tournaments.filter(({ Turneringstype }) => norwegianTournamentsTypes.includes(Turneringstype))
}

function getData(result) {
    if(result.status !== 200) {
        throw new Error(`Did not get a 200 response from API, details: ${result}`)
    }
    return result.data
}


